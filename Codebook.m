
## Codebook for the course project

### Run_analysis_2.R

The result of the R script are two datasets: completeData and summary_Data.
The following is an explanation of the variables in the datasets.

### completeData

All the variables on the dataset are numeric except for the "Activity" colum, which is of class "char"

    *Subject: A number that identifies the person during the experiment.
    *Activity: Type of activity that the person was doing.
 
For the following variables the mean for each axis (X,Y,Z) and standard deviation was obtained and put in the resulting dataset.
Also, there are two types of signals for the measurements. 

BodyAcc - X,Y,Z
GravityAcc - X,Y,Z
BodyAccJerk - X,Y,z
BodyGyro - X,Y,Z
BodyGyroJerk - X,Y,Z

      
